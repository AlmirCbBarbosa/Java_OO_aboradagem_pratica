package capitulo_3.exemplo2;
//Programa Java mostrando declaração de variáveis
public class Exemplo2{
    public static void main(String[] args){
        int a = 0;
        int b = 2;

        int soma = a + b;
        System.out.println("a soma foi: " + soma);
    }
}

